# ragwithtopk/experiments/config.py
from __future__ import annotations
from dataclasses import dataclass
from typing import List, Optional

@dataclass
class ExperimentConfig:
    run_name: str
    reasoning_type: str
    needle_set_path: str
    text_files: List[str]          # explicit list or generated by glob in CLI
    base_seed: int                 # “global seed”; per-file seeds derived from this
    openai_api_key: str
    embedding_model: str = "text-embedding-3-small"
    embedding_batch_size: int = 16
    
    qdrant_url: str = "http://localhost:6333"
    qdrant_api_key: Optional[str] = None
    qdrant_collection: str = "rag_topk_nolima"

    output_dir: str = "NoLiMa_based_RAG/data"
